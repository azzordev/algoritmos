# cadena de texto
nombre_de_persona = "Juan Felipe"
print(nombre_de_persona)

# numero
edad_de_persona = 22
print(edad_de_persona)

# booleano
estado_de_persona = False
print(estado_de_persona)

# arreglo
edad_personas = [22, 19, 18, 22, 20]
print(edad_personas)
print(edad_personas[0])
print(edad_personas[1])

# objeto
objeto_persona = {
    "nombre": "Juan Felipe",
    "edad": 22,
    "estado_salud": True,
}
print(objeto_persona)

# arreglo de objetos
informacion_de_personas = [
    {
        "nombre": "Juan Felipe",
        "edad": 22,
        "estado_salud": True,
    },
    {
        "nombre": "Sebastian",
        "edad": 19,
        "estado_salud": True,
    },
]
print(informacion_de_personas)
